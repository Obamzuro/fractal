#include "./../inc/fractol.h"

void	ft_putpixel(t_mlx *p, int x, int y, int color)
{
	if (x < 0 || x > W_W - 1 || y < 0 || y > W_H - 1)
		return ;
	p->p_screen[y * W_W + x] = color;
}
